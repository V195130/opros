<br>
  CERN – неграфический редактор;
  <br>
 NCSA Mosaic – первый, получивший широкое распространение графический браузер, который резко изменил представление об Интернете. Именно эта программа преобразовала Интернет в паутину. Представляет историческую ценность;
 <br>
 Cello – один из самых экономичных браузеров с точки зрения использования ресурсов компьютера;
 <br>
 Netscape Navigator и Microsoft Internet Explorer – наиболее известные и конструктивные браузеры. Они обладают сейчас самыми широкими возможностями при отображении Web-страниц.
